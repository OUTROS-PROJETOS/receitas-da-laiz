function getRecipeTitleFromPath(path) {
    const fileName = path.split('/').pop() || '';
    return fileName
        .replace('.jpg', '')
        .replace(/^(prato|doce|salgado|molho)-\s?/i, '')
        .replace(/\s+/g, ' ')
        .trim()
        .replace(/\b\w/g, (letter) => letter.toUpperCase());
}

export const recipes = recipesRaw.map((recipe, index) => ({
    id: `${recipe.category}-${index + 1}`,
    ...recipe,
    title: getRecipeTitleFromPath(recipe.src),
}));

export const recipesByCategory = recipes.reduce((acc, recipe) => {
    if (!acc[recipe.category]) {
        acc[recipe.category] = [];
    }
    acc[recipe.category].push(recipe);
    return acc;
}, {});
